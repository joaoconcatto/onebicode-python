import math

# 1- Acessar o número Pi
print(f"{math.pi:.2f}")


# 2- Acessar o número de Euler
print(f'{math.e:.2f}')


# 3- Arredondando números
num1 = 10.4
print(math.ceil(num1))
print(math.floor(num1))


# 4- Fatorial de um número
num2 = 7
print(math.factorial(num2))


# 5- Potência de números
num3 = 6
print(math.pow(num3, 2))


# 6- Raiz quadrada de um número
num4 = 4
print(math.sqrt(num4))


# 7- MDC
mdc = math.gcd(25,100)
print(mdc)


# 8- Logatitmo
print(math.log(10))